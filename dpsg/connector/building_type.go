package connector

import (
	"golang-project/dpsg/rpc"
)

type Movable interface {
	GetP() *rpc.Position
	SetP(p *rpc.Position)
}

type Upgradable interface {
	GetUpgradeTime() uint32
	SetUpgradeTime(now uint32)
	GetLevel() uint32
	SetLevel(l uint32)
}

type Removable interface {
	GetRemoveTime() uint32
	SetRemoveTime(now uint32)
	GetGemNum() uint32
	SetGemNum(value uint32)
}

type Operable interface {
	GetLastOpTime() uint32
	SetLastOpTime(value uint32)
}

type Assailable interface {
	GetHp() uint32
	SetHp(value uint32)
}

type CanStorageGold interface {
	SetStorageGold(value uint32)
	GetStorageGold() uint32
}

type CanStorageFood interface {
	SetStorageFood(value uint32)
	GetStorageFood() uint32
}
